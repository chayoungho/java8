package Day20;

import java.awt.Graphics;

import javax.swing.JApplet;

public class ParamJApplet extends JApplet{
	private static final long seriaIVersionUID =1L;  //클래스의 고유넘버를 지정
	String title;									 //클래스형 객체의 데이터 전송시						
													 //송신부에서 동일 클래스임을 확인하기
	public void init(String title) {				 //위하여 사용합니다.
		this.title=title;							 //명시하지 않으면 랜덤으로 생성합니다.
		if(title==null)
			title="전달받은 인수가 없음.";
	}
	public void paint(Graphics g) {
		g.drawString(title, 100, 100);
	}
}
