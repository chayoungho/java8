package Day18;

public class Student02 {
	private String name;
	private String sex;
	private int score;
	public Student02(String name, String sex, int score) {
		this.name = name;
		this.sex = sex;
		this.score = score;
	}
	public String getName() {
		return name;
	}
	public String getSex() {
		return sex;
	}
	public int getScore() {
		return score;
	}
	
	
}
